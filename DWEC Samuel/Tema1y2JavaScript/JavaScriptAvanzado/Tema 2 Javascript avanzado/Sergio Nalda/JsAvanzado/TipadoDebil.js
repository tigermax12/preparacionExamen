let variable; // Se declara la variable "variable" sin asignarle un valor inicial. Su valor es "undefined".

typeof variable; // Se usa "typeof" para verificar el tipo de la variable. Como no tiene un valor asignado, su tipo es "undefined".

variable = 5; // Se asigna el valor num√©rico 5 a la variable. Ahora "variable" tiene el tipo "number".

typeof variable; // Se vuelve a usar "typeof" para verificar el tipo de la variable. Ahora es "number" porque su valor es 5.

variable = "hola"; // Se asigna una cadena de texto ("hola") a la variable. Ahora "variable" tiene el tipo "string".

